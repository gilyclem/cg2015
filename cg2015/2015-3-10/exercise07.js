//exercise07 write a function that randomizes a number in range (0..100), then prints all the odd numbers from 40 to that one. If the number was smaller than 40 nothing should be printed

(function(){

var num = Math.floor(Math.random()*100+1);

var res="";
if (num>40)		{
	for (var i=41;i<num;i+=2) 
		res+=i+" ";	
			}

console.log(num+"--->"+res);
console.log("\n");
}());
